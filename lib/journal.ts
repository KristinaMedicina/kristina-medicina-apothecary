export function getArticle(slug: string): Article | undefined {
  return articles.find((a) => a.slug === slug);
}

/** All articles, newest first. */
export function getArticles(): Article[] {
  return [...articles].sort(
    (a, b) =>
      new Date(b.publishedAt).getTime() - new Date(a.publishedAt).getTime(),
  );
}

export function getArticlesByCategory(category: string): Article[] {
  return getArticles().filter((a) => a.category === category);
}

export function getFeaturedArticles(limit = 3): Article[] {
  const featured = getArticles().filter((a) => a.featured);
  return (featured.length ? featured : getArticles()).slice(0, limit);
}

/** The single lead/featured article for the journal hero. */
export function getLeadArticle(): Article {
  return getFeaturedArticles(1)[0];
}

export function getCategory(slug: string): JournalCategoryMeta | undefined {
  return journalCategories.find((c) => c.slug === slug);
}
